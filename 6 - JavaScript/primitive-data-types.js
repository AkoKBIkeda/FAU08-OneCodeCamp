// Primitive Data Type

// String: Textual data enclosed in '' or ""
let firstName = "John";
console.log("First Name: ", firstName);
console.log("Type of First Name: ", typeof firstName);

// Number: Represents numeric value (30, -30, 3.14)
let age = 30;
console.log("Age: ", age);
console.log("Type of Age: ", typeof age);

// Boolean: 
let isStudent = true;
console.log("Are they student?: ", isStudent);
console.log("Type of isStudent?: ", typeof isStudent);

// Null: Intentional absence of value
let car = null;
console.log("Car: ", car);
console.log("Type of Car: ", typeof car);

// Undefined: Unintentional absense of value
let city;
console.log("City: ", city);
console.log("Type of City: ", typeof city);